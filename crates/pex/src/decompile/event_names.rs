/// Whether `name` is a built-in engine event (case-insensitive). The
/// caller has already checked the `on` prefix, matching Champollion.
pub(crate) fn is_event_name(name: &str) -> bool {
    let lower = name.to_ascii_lowercase();
    EVENT_NAMES.binary_search(&lower.as_str()).is_ok()
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn list_is_sorted_for_binary_search() {
        assert!(EVENT_NAMES.windows(2).all(|w| w[0] < w[1]));
    }

    #[test]
    fn known_events_match_case_insensitively() {
        assert!(is_event_name("OnActivate"));
        assert!(is_event_name("onactivate"));
        assert!(is_event_name("OnInit"));
        assert!(is_event_name("OnUpdate"));
        assert!(!is_event_name("MyHelperFunction"));
        assert!(!is_event_name("OnSomethingNobodyDefined"));
    }
}
